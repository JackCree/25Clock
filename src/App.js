//accurate interval : allow to add a break at the end of the clock runner 
const accurateInterval = function (fn, time) {
  var cancel, nextAt, timeout, wrapper;
  //nextAt : Length of the break between 2 run 
    nextAt = new Date().getTime() + time;
    timeout = null;
    wrapper = function () {
      nextAt += time;
      timeout = setTimeout(wrapper, nextAt - new Date().getTime());
      return fn();
    };
  cancel = function () {
    return clearTimeout(timeout);
  };
  timeout = setTimeout(wrapper, nextAt - new Date().getTime());
  return {
    cancel: cancel
  };
};

